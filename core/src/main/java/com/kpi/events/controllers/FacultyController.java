package com.kpi.events.controllers;

import com.kpi.events.model.Faculty;
import com.kpi.events.services.FacultyService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.autoconfigure.condition.ConditionalOnProperty;
import org.springframework.web.bind.annotation.*;

@RestController
@RequestMapping("/api")
@ConditionalOnProperty(name = "features.university")
public class FacultyController {

    @Autowired
    private FacultyService service;

    @GetMapping("/facultyInfo/{id}")
    public Faculty getFaculty(@PathVariable("id") long studentId) {
        return service.findByStudentId(studentId);
    }
}

